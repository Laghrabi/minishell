#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		j;
	int		i;
	char	*p;

	if (s1 == NULL)
		return (NULL);
	i = ft_strlen(s1);
	p = gc_malloc((i + 1) * sizeof(char));
	if (!p)
		return (NULL);
	j = 0;
	while (j < i)
	{
		p[j] = s1[j];
		j++;
	}
	p[j] = '\0';
	return (p);
}
